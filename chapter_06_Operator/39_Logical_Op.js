// && -> AND Gate
// || -> OR Gate

let a = true;
let b = false;
console.log(a && b); //  AND  //FALSE
console.log(a || b); //  OR   // TRUE
console.log(!a); // false

console.log(5 != "g");  // Value 5, g ! that true,  //true
console.log(5 !== "5"); // Value 5, type number, "5" type string ! that true,  //true